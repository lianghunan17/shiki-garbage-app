// カテゴリー一覧を取得する関数
export const getCategories = (): string[] => {
  const categories = [...new Set(bulkyWasteData.map(item => item.category))];
  return categories;
};

// 品目名で検索する関数
export const searchByName = (searchTerm: string): BulkyWasteItem[] => {
  if (!searchTerm.trim()) return [];
  
  return bulkyWasteData.filter(item => 
    item.name.toLowerCase().includes(searchTerm.toLowerCase())
  );
};

// カテゴリーで絞り込む関数
export const filterByCategory = (category: string): BulkyWasteItem[] => {
  if (!category) return bulkyWasteData;
  
  return bulkyWasteData.filter(item => item.category === category);
};

// 料金で絞り込む関数
export const filterByFee = (minFee?: number, maxFee?: number): BulkyWasteItem[] => {
  return bulkyWasteData.filter(item => {
    if (minFee !== undefined && item.fee < minFee) return false;
    if (maxFee !== undefined && item.fee > maxFee) return false;
    return true;
  });
};